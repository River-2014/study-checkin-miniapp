/**
 * 第一层：前置约束校验
 * 在请求发送前检查 prompt 配置是否完备
 * @param {Object} promptConfig - { grade, subject, knowledgePoints, questionTypes, difficulty }
 * @returns {Object} { valid: boolean, reason?: string }
 */
function validatePrompt(promptConfig) {
  if (!promptConfig) return { valid: false, reason: 'prompt配置为空' };
  if (!promptConfig.grade) return { valid: false, reason: '年级未设置' };
  if (!promptConfig.subject) return { valid: false, reason: '学科未设置' };
  if (!promptConfig.knowledgePoints || promptConfig.knowledgePoints.length === 0) {
    return { valid: false, reason: '知识点未选择' };
  }
  if (!promptConfig.questionTypes || promptConfig.questionTypes.length === 0) {
    return { valid: false, reason: '题型未选择' };
  }
  if (!promptConfig.difficulty) return { valid: false, reason: '难度未设置' };

  // 校验知识点是否在对应年级学科的白名单内
  for (var i = 0; i < promptConfig.knowledgePoints.length; i++) {
    if (!knowledgeBase.isKnowledgeValid(promptConfig.grade, promptConfig.subject, promptConfig.knowledgePoints[i])) {
      return { valid: false, reason: '知识点"' + promptConfig.knowledgePoints[i] + '"不在该年级白名单中' };
    }
  }

  return { valid: true };
}

/**
 * 第二层：实时流式过滤
 * 在 SSE 流式接收过程中对已解析的题目做快速过滤
 * @param {string} accumulatedContent - 当前累积的流式文本
 * @param {string} grade - 年级，如'一年级'
 * @returns {Object} { passed: boolean, question?: Object, reason?: string }
 */
function interceptStreamingChunk(accumulatedContent, grade) {
  if (!accumulatedContent || !grade) return { passed: false, reason: '参数不足' };

  var forbidden = knowledgeBase.getForbiddenWords(grade);
  if (!forbidden || forbidden.length === 0) return { passed: true };

  // 扫描累积内容中的超纲词汇
  for (var i = 0; i < forbidden.length; i++) {
    if (accumulatedContent.indexOf(forbidden[i]) !== -1) {
      return { passed: false, reason: '超纲词:' + forbidden[i] };
    }
  }

  return { passed: true };
}

/**
 * 第三层：后置结构化核验
 * 对完整的题目对象进行全面校验
 * @param {Object} question - 题目对象 { type, stem, options, answer, knowledgePoint, explanation, audioText }
 * @param {string} grade - 年级
 * @param {string} subject - 学科
 * @returns {Object} { valid: boolean, reason?: string }
 */
function validateQuestion(question, grade, subject) {
  if (!question) return { valid: false, reason: '题目对象为空' };
  if (!question.type) return { valid: false, reason: '题型缺失' };
  if (!question.stem && !question.content) return { valid: false, reason: '题干缺失' };

  var formats = knowledgeBase.getSubjectFormats(subject);

  // 1. 知识点白名单校验（仅当知识点字段存在时执行）
  if (question.knowledgePoint) {
    if (!knowledgeBase.isKnowledgeValid(grade, subject, question.knowledgePoint)) {
      return { valid: false, reason: '知识点无效:' + question.knowledgePoint };
    }
  }

  // 2. 超纲词汇扫描
  var combined = (question.stem || question.content || '') + ' ' +
                  (question.analysis || question.explanation || '') + ' ' +
                  (question.examPoint || '');
  var forbidden = knowledgeBase.getForbiddenWords(grade);
  for (var f = 0; f < forbidden.length; f++) {
    if (forbidden[f].length >= 2 && combined.indexOf(forbidden[f]) !== -1) {
      return { valid: false, reason: '超纲词:' + forbidden[f] };
    }
  }

  // 3. 年级生字库过滤 — 仅对语文科目且仅 warn 不 reject
  var stemText = question.stem || question.content || '';
  if (subject === '语文' && stemText.length > 0) {
    var charSet = getGradeCharSet(grade);
    if (charSet) {
      var outOfRange = 0;
      var totalHan = 0;
      for (var c = 0; c < stemText.length; c++) {
        var ch = stemText[c];
        if (ch >= '一' && ch <= '鿿') {
          totalHan++;
          if (charSet.indexOf(ch) === -1) {
            outOfRange++;
          }
        }
      }
      if (totalHan > 0 && (outOfRange / totalHan) > 0.2) {
        console.warn('antiHallucination: 语文题干含较多超纲汉字', outOfRange + '/' + totalHan);
        // 不拦截，仅警告
      }
    }
  }

  // 4. 题型格式校验（宽松模式）
  if (question.type === '选择题') {
    // 兼容对象格式 options: { "A": "...", "B": "...", "C": "...", "D": "..." }
    var normalizedOptions = normalizeOptions(question.options);
    if (!normalizedOptions || normalizedOptions.length < 2) {
      // 不拦截，只标记，AI 可能内嵌选项在 content 中
      console.warn('antiHallucination: 选择题选项格式异常');
    }

    // 校验答案是否有效（不修改原值，ai-exam.js 的 isCorrect 已支持字母比较）
    if (question.answer !== undefined && question.answer !== null) {
      var normalizedAns = normalizeAnswer(question.answer);
      if (normalizedAns === undefined || normalizedAns === null) {
        console.warn('antiHallucination: 选择题答案格式异常');
      }
    }
  }

  if (question.type === '填空题') {
    if (!question.answer || (typeof question.answer === 'string' && question.answer.trim() === '')) {
      // 填空题 answer 可为空（部分开放型填空题），不强制拦截
      console.warn('antiHallucination: 填空题答案为空');
    }
  }

  if (question.type === '应用题') {
    if (!question.explanation && !question.analysis && !question.examPoint) {
      // 应用题缺少解析不强制拦截，仅警告
      console.warn('antiHallucination: 应用题缺少解析');
    }
  }

  if (question.type === '听力填空' || question.type === '听力题') {
    if (!question.audioText && !question.listeningScript) {
      return { valid: false, reason: '听力题缺少音频文本' };
    }
    var audioText = question.audioText || question.listeningScript || '';
    if (audioText.length < 5) {
      return { valid: false, reason: '听力文本内容过短' };
    }
  }

  // 5. 学科答案合理性校验（仅对数学学科执行）
  if (subject === '数学' && question.answer !== undefined && question.answer !== null) {
    var answerContent = question.answer + '';
    var answerCheck = knowledgeBase.validateAnswerBySubject(subject, question.stem || question.content, answerContent);
    if (!answerCheck.valid) {
      return answerCheck;
    }
  }

  return { valid: true };
}

/**
 * 将选择题 options 统一为数组格式
 * 支持：["A", "B", "C", "D"] 或 {A:"...", B:"...", C:"...", D:"..."}
 */
function normalizeOptions(options) {
  if (!options) return null;
  if (Array.isArray(options)) return options;
  if (typeof options === 'object') {
    var keys = Object.keys(options);
    // 按 A/B/C/D 键排序
    var sortOrder = { 'A': 0, 'B': 1, 'C': 2, 'D': 3, 'a': 0, 'b': 1, 'c': 2, 'd': 3 };
    keys.sort(function(a, b) {
      var ia = sortOrder[a] !== undefined ? sortOrder[a] : 99;
      var ib = sortOrder[b] !== undefined ? sortOrder[b] : 99;
      return ia - ib;
    });
    return keys.map(function(k) { return options[k]; });
  }
  return null;
}

/**
 * 将选择题答案统一为数字索引
 * 支持 0, "0", "A", "B", "A)", "(A)" 等
 */
function normalizeAnswer(answer) {
  if (typeof answer === 'number') return answer;
  if (typeof answer !== 'string') return answer;
  var str = answer.trim().toUpperCase();
  var mapping = { 'A': 0, 'B': 1, 'C': 2, 'D': 3 };
  if (mapping[str] !== undefined) return mapping[str];
  // 处理 "A)"、"(A)"、"A." 等变体
  var match = str.match(/[A-D]/);
  if (match && mapping[match[0]] !== undefined) return mapping[match[0]];
  var num = parseInt(str, 10);
  if (!isNaN(num) && num >= 0 && num <= 3) return num;
  return answer;
}

/**
 * 批量校验题目数组
 * @param {Array} questions - 题目数组
 * @param {string} grade - 年级
 * @param {string} subject - 学科
 * @returns {Array} 有效的题目数组
 */
function filterValidQuestions(questions, grade, subject) {
  if (!questions || questions.length === 0) return [];
  var result = [];
  for (var i = 0; i < questions.length; i++) {
    var check = validateQuestion(questions[i], grade, subject);
    if (check.valid) {
      result.push(questions[i]);
    }
  }
  return result;
}

